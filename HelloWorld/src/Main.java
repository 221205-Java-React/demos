public class Main{

    //this is the main method - the starting/entry point for any Java application
    //when your java application starts, the main method is what runs first
    public static void main(String[] args){

        //a print statement lets us "print" something to the console
        //This lets you make stuff appear in the box that pops up when you run your application
        System.out.println("Hello World");

        /*
           System = console
           out = output
           Print = make this appear/visible to where I tell you
           ln = print this on a line
        */

        //sout is the shortcut to make a print statement
        //but PLEASE remember how to actually write it
        System.out.println();

        //the main method has a shortcut too - main

    }

}